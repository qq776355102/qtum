package js_java;

public class User {

	public int id;
	
	public String uname;
	
	public String address;



	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}



	public String getUname() {
		return uname;
	}

	public void setUname(String uname) {
		this.uname = uname;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}
	
}
